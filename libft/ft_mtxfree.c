#include "libft.h"

void	*ft_mtxfree(char **mtx)
{
	int	i;

	if (mtx)
	{
		i = -1;
		while (mtx[++i])
			mtx[i] = ft_free(mtx[i]);
		free(mtx);
	}
	return (NULL);
}
